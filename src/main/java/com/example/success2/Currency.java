package com.example.success2;

public class Currency {
    private String code;
    private String name;
    private long value;

    // Getters and Setters

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getValue(){
        return value;
    }

    public void setValue(){
        this.value = value;
    }
}